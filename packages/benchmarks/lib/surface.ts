function surfaceStats(payload) {
  const serialized = JSON.stringify(payload);
  return {
    toolCount: payload.tools.length,
    bytes: Buffer.byteLength(serialized, "utf8"),
    approxTokens: Math.ceil(Buffer.byteLength(serialized, "utf8") / 4),
  };
}

function duplicateNameCollisions(tools) {
  const grouped = new Map();
  for (const tool of tools) {
    const entry = grouped.get(tool.name) ?? { name: tool.name, count: 0, servers: new Set() };
    entry.count += 1;
    if (tool.server) {
      entry.servers.add(tool.server);
    }
    grouped.set(tool.name, entry);
  }
  return [...grouped.values()]
    .filter((entry) => entry.count > 1)
    .map((entry) => ({ name: entry.name, count: entry.count, servers: [...entry.servers].sort() }))
    .sort((a, b) => a.name.localeCompare(b.name));
}

function capletTool(server) {
  return {
    name: server.server,
    title: server.name,
    description: capabilityDescription(server),
    inputSchema: generatedToolInputJsonSchema(),
  };
}

function benchmarkServerName(server) {
  return `${titleCase(server)} benchmark Caplet`;
}

function benchmarkServerDescription(server) {
  return `Deterministic ${server} MCP server used for coding-agent benchmark discovery.`;
}

function titleCase(value) {
  return `${value.slice(0, 1).toUpperCase()}${value.slice(1)}`;
}

function percent(value) {
  return `${(value * 100).toFixed(1)}%`;
}
