import { capabilityDescription } from "../../core/src/capability-description.js";
import { generatedToolInputJsonSchema } from "../../core/src/generated-tool-input-schema.js";
import { listToolMetadata, SERVER_NAMES } from "../fixtures/mcp-server.js";

export const SURFACE_THRESHOLDS = {
  minInitialPayloadReduction: 0.7,
};

export function benchmarkServerDefinitions() {
  return Object.fromEntries(
    SERVER_NAMES.map((server) => [
      server,
      {
        name: benchmarkServerName(server),
        description: benchmarkServerDescription(server),
        command: "tsx",
        args: ["fixtures/mcp-server.ts", "--server", server],
      },
    ]),
  );
}

export function directFlatTools() {
  return SERVER_NAMES.flatMap((server) =>
    listToolMetadata(server).map((tool) => ({
      name: tool.name,
      title: tool.title,
      description: tool.description,
      inputSchema: tool.inputSchema,
      annotations: tool.annotations,
      hasOutputSchema: Boolean(tool.outputSchema),
      outputSchema: tool.outputSchema,
      server: tool.server,
    })),
  );
}

export function directFlatPayload() {
  return { tools: directFlatTools() };
}

export async function capletsTopLevelPayload() {
  const mcpServers = normalizedBenchmarkServers();
  return {
    payload: {
      tools: Object.values(mcpServers).map((server) => capletTool(server)),
    },
    implementation: "source",
  };
}

function normalizedBenchmarkServers() {
  return Object.fromEntries(
    Object.entries(benchmarkServerDefinitions()).map(([server, definition]) => [
      server,
      {
        ...definition,
        server,
        backend: "mcp",
        transport: "stdio",
        startupTimeoutMs: 10_000,
        callTimeoutMs: 30_000,
        toolCacheTtlMs: 30_000,
        disabled: false,
      },
    ]),
  );
}

export async function computeSurfaceBenchmark() {
  const directPayload = directFlatPayload();
  const { payload: capletsPayload, implementation } = await capletsTopLevelPayload();
  const direct = surfaceStats(directPayload);
  const caplets = surfaceStats(capletsPayload);
  const directCollisions = duplicateNameCollisions(directPayload.tools);
  const topLevelCollisions = duplicateNameCollisions(capletsPayload.tools);
  const initialPayloadReduction = 1 - caplets.bytes / direct.bytes;
  const initialToolReduction = 1 - caplets.toolCount / direct.toolCount;

  return {
    // Fixed timestamp keeps deterministic benchmark reports reproducible.
    generatedAt: new Date(0).toISOString(),
    source: {
      servers: SERVER_NAMES,
      capletsPayloadImplementation: implementation,
    },
    direct,
    caplets,
    reductions: {
      initialPayloadReduction,
      initialPayloadReductionPercent: percent(initialPayloadReduction),
      initialToolReduction,
      initialToolReductionPercent: percent(initialToolReduction),
      candidateSetReduction: direct.toolCount - caplets.toolCount,
      candidateSetReductionRatio: direct.toolCount / caplets.toolCount,
    },
    collisions: {
      directDuplicateToolNames: directCollisions,
      directDuplicateToolNameCount: directCollisions.length,
      capletsTopLevelDuplicateToolNames: topLevelCollisions,
      capletsTopLevelDuplicateToolNameCount: topLevelCollisions.length,
    },
    discovery: {
      directExpectedDiscoveryCalls: 0,
      capletsExpectedDiscoveryCalls: {
        inspectCaplet: 1,
        discoverTools: 1,
        inspectTool: 1,
        callTool: 1,
        total: 4,
      },
    },
    thresholds: SURFACE_THRESHOLDS,
  };
}

export function validateSurfaceBenchmark(result) {
  const failures = [];
  if (result.reductions.initialPayloadReduction < SURFACE_THRESHOLDS.minInitialPayloadReduction) {
    failures.push(
      `Caplets initial payload reduction ${percent(result.reductions.initialPayloadReduction)} is below ${percent(SURFACE_THRESHOLDS.minInitialPayloadReduction)}.`,
    );
  }
  if (result.collisions.capletsTopLevelDuplicateToolNameCount !== 0) {
    failures.push("Caplets top-level payload has duplicate tool-name collisions.");
  }
  return failures;
}

