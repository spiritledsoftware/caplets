function extractDeclaration(source: string, declaration: string): string | undefined {
  const match = new RegExp(`(^|\\n)${escapeRegExp(declaration)}(?=[<\\s=])`).exec(source);
  if (!match) return undefined;
  const start = match.index + (match[1] ? match[1].length : 0);
  const lineEnd = source.indexOf("\n", start);
  const declarationLine = source.slice(start, lineEnd === -1 ? undefined : lineEnd);
  if (!declarationLine.includes("{")) {
    if (declarationLine.trimEnd().endsWith(";")) {
      return declarationLine.trim();
    }
    const nextDeclaration = /\n(?:interface|type)\s+[A-Za-z_$]/u.exec(source.slice(lineEnd + 1));
    const end = nextDeclaration ? lineEnd + 1 + nextDeclaration.index : source.length;
    return source.slice(start, end).trim();
  }

  let index = start;
  let depth = 0;
  let seenBrace = false;
  while (index < source.length) {
    const char = source[index];
    if (char === "{") {
      depth++;
      seenBrace = true;
    } else if (char === "}") {
      depth--;
      if (seenBrace && depth === 0) {
        const semicolon = source[index + 1] === ";" ? index + 2 : index + 1;
        return source.slice(start, semicolon).trim();
      }
    }
    index++;
  }

  return undefined;
}

function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function schemaType(schema: JsonSchema): string {
  if (schema.$ref) return schema.$ref.split("/").at(-1) ?? schema.$ref;
  if (schema.enum) return schema.enum.map((value) => JSON.stringify(value)).join(" | ");
  if (schema.type) return Array.isArray(schema.type) ? schema.type.join(" | ") : schema.type;
  if (schema.oneOf) return uniqueTypes(schema.oneOf, " | ");
  if (schema.anyOf) return uniqueTypes(schema.anyOf, " | ");
  if (schema.allOf) return uniqueTypes(schema.allOf, " & ");
  if (schema.additionalProperties && typeof schema.additionalProperties === "object") {
    return `record of ${schemaType(schema.additionalProperties)}`;
  }
  if (schema.properties) return "object";
  if (schema.items) return `${schemaType(schema.items)}[]`;
  return "unknown";
}

function uniqueTypes(schemas: JsonSchema[], separator: string): string {
  return Array.from(new Set(schemas.map(schemaType))).join(separator);
}

function fieldDescription(field: string, schema: JsonSchema): string {
  if (schema.description) return schema.description;
  return (
    fallbackFieldDescriptions.get(field) ??
    (schema.type === "array"
      ? `Optional list of ${field}.`
      : "See the canonical schema for details.")
  );
}

function readJsonSchema(path: string): JsonSchema {
  return JSON.parse(readFileSync(join(repoRoot, path), "utf8")) as JsonSchema;
}

function frontmatter(title: string, description: string): string {
  return `---\ntitle: ${JSON.stringify(title)}\ndescription: ${JSON.stringify(description)}\n---\n`;
}

function escapeTable(value: string): string {
  return value
    .replaceAll("|", "\\|")
    .replaceAll("{", "\\{")
    .replaceAll("}", "\\}")
    .replaceAll("\n", " ");
}

function formatMdx(value: string): string {
  const dir = mkdtempSync(join(tmpdir(), "caplets-docs-"));
  const path = join(dir, "page.mdx");
  try {
    writeFileSync(path, value);
    execFileSync("pnpm", ["exec", "oxfmt", path], { stdio: "inherit" });
    return readFileSync(path, "utf8");
  } finally {
    rmSync(dir, { recursive: true, force: true });
  }
}

function findRepoRoot(start: string): string {
  let current = resolve(start);
  while (!existsSync(join(current, "pnpm-workspace.yaml"))) {
    const parent = dirname(current);
    if (parent === current) {
      throw new Error(`Could not find repository root from ${start}`);
    }
    current = parent;
  }
  return current;
}
