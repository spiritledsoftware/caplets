function schemaPage({
  title,
  description,
  sourcePath,
  canonicalUrl,
  intro,
}: {
  title: string;
  description: string;
  sourcePath: string;
  canonicalUrl: string;
  intro: string;
}): string {
  const schema = readJsonSchema(sourcePath);
  const required = new Set(schema.required ?? []);
  const rows = Object.entries(schema.properties ?? {}).map(([name, property]) => ({
    name,
    status: required.has(name) ? "Required" : "Optional",
    type: schemaType(property),
    description: fieldDescription(name, property),
  }));

  const majorSections = rows.filter(({ name }) =>
    [
      "completion",
      "options",
      "mcpServers",
      "openapiEndpoints",
      "googleDiscoveryApis",
      "graphqlEndpoints",
      "httpApis",
      "cliTools",
      "capletSets",
      "setup",
      "projectBinding",
      "runtime",
      "mcpServer",
      "openapiEndpoint",
      "googleDiscoveryApi",
      "graphqlEndpoint",
      "httpApi",
      "capletSet",
    ].includes(name),
  );

  return `${frontmatter(title, description)}
${generatedMarkerComment}

${intro}

Canonical schema: [${canonicalUrl}](${canonicalUrl})

Status values are \`Required\` or \`Optional\` according to the schema's top-level \`required\` array.

${commonSchemaRecipes(sourcePath)}

## Top-level fields

| Field | Status | Type | Description |
| --- | --- | --- | --- |
${rows.map((row) => `| \`${escapeTable(row.name)}\` | ${row.status} | ${escapeTable(row.type)} | ${escapeTable(row.description)} |`).join("\n")}

## Major sections

${majorSections.map((row) => sectionDetails(row.name, schema.properties?.[row.name], sourcePath)).join("\n\n")}
`;
}

function sectionDetails(name: string, schema: JsonSchema | undefined, sourcePath: string): string {
  if (!schema) return `### \`${name}\`\n\nNo generated details are available.`;
  if (sourcePath === "schemas/caplet.schema.json" && name === "cliTools") {
    return cliToolsCapletFileSection(schema);
  }

  const nested =
    schema.properties ??
    schema.items?.properties ??
    (typeof schema.additionalProperties === "object"
      ? schema.additionalProperties.properties
      : undefined);
  if (!nested) {
    return `### \`${name}\`\n\n${schema.description ?? "See the canonical schema for the full nested shape."}`;
  }

  const required = new Set(
    schema.required ??
      schema.items?.required ??
      (typeof schema.additionalProperties === "object"
        ? schema.additionalProperties.required
        : undefined) ??
      [],
  );
  const rows = Object.entries(nested).map(([field, property]) => {
    const status = required.has(field) ? "Required" : "Optional";
    const description = fieldDescription(field, property);
    return `| \`${escapeTable(field)}\` | ${status} | ${escapeTable(schemaType(property))} | ${escapeTable(description)} |`;
  });

  return `### \`${name}\`

${schema.description ?? "Nested fields from the canonical schema."}

| Field | Status | Type | Description |
| --- | --- | --- | --- |
${rows.join("\n")}`;
}

