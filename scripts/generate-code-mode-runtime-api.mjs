#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const root = dirname(dirname(fileURLToPath(import.meta.url)));
const check = process.argv.includes("--check");
const declarations = [
  {
    template: "packages/core/src/code-mode/runtime-api.d.ts",
    output: "packages/core/src/code-mode/runtime-api.generated.ts",
    exportName: "CODE_MODE_RUNTIME_API_DECLARATION",
  },
  {
    template: "packages/core/src/code-mode/diagnostics-builtins.d.ts",
    output: "packages/core/src/code-mode/diagnostics-builtins.generated.ts",
    exportName: "CODE_MODE_DIAGNOSTICS_BUILTINS_DECLARATION",
  },
];

const generatedDeclarations = declarations.map(({ template, output, exportName }) => {
  const declaration = minifyDeclarationText(readFileSync(join(root, template), "utf8"));
  const generated = [
    "// Generated by scripts/generate-code-mode-runtime-api.mjs. Do not edit by hand.",
    `export const ${exportName} =`,
    `  '${singleQuoted(declaration)}' as const;`,
    "",
  ].join("\n");
  return { output, generated };
});

if (check) {
  for (const { output, generated } of generatedDeclarations) {
    const current = readFileSync(join(root, output), "utf8");
    if (current !== generated) {
      console.error(`${output} is out of date. Run pnpm code-mode:generate-api.`);
      process.exitCode = 1;
    }
  }
} else {
  for (const { output, generated } of generatedDeclarations) {
    writeFileSync(join(root, output), generated);
  }
}

function singleQuoted(value) {
  return value.replace(/\\/gu, "\\\\").replace(/'/gu, "\\'");
}

function minifyDeclarationText(value) {
  return value
    .replace(/^\s*export\s*\{\s*\}\s*;?\s*/u, "")
    .replace(/\s*export\s*\{\s*\}\s*;?\s*$/u, "")
    .replace(/\r\n?/gu, "\n")
    .split("\n")
    .map((line) => line.trim())
    .filter(Boolean)
    .join(" ")
    .replace(/\s+/gu, " ")
    .replace(/\s*([{}()[\]:;,|&=])\s*/gu, "$1")
    .replace(/\s*<\s*/gu, "<")
    .replace(/\s*>\s*/gu, ">")
    .replace(/\?\s*:/gu, "?:")
    .trim();
}
